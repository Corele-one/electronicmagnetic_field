%%点电荷位于接地导体球外
%假设半径为a=1cm的接地导体球外有一个点电荷q=1x10^(-10)C,点电荷距离球心d=2cm,求球外的电位与电场强度分布。
clear
clc
k = 8.985e9;
ra = 0.01;
d = 0.02;
q1 = 1e-10;
q2 = -ra/d*q1;%q2为镜像点电荷
x = linspace(-0.06,0.06,100);
[X,Y] = meshgrid(x);
b = ra^2/d;%镜像电荷的位置

r1 = sqrt((X - d).^2 + Y.^2); %点电荷到场点的距离
r2 = sqrt((X - b).^2 + Y.^2); %镜像电荷到场点的距离
U = k*q1./r1 + k*q2./r2;
